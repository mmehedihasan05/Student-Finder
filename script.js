let form = document.querySelector("form");
let output = document.querySelector(".output");

let lastSearchedData = { std_roll: "", std_name: "Search your friend" };

form.addEventListener("submit", (e) => {
    e.preventDefault();

    let inputtedRoll = document.querySelector("#roll_input");

    console.log(inputtedRoll.value);

    inputtedRoll = Number(inputtedRoll.value);

    console.log(inputtedRoll);

    lastSearchedData["std_roll"] = inputtedRoll;
    let tmpName = studentRollName[inputtedRoll];
    lastSearchedData["std_name"] = tmpName;

    if (studentRollName[inputtedRoll] == undefined) {
        output.innerHTML = `No Studnt Found!`;
    } else {
        output.innerHTML = `
        <table>
                <tr>
                    <td>Roll </td>
                    <td class="outputRoll">: ${inputtedRoll}</td>
                </tr>
                <tr>
                    <td>Name</td>
                    <td class="outputName">: ${studentRollName[inputtedRoll]}</td>
                </tr>
                <tr><td class="copyText" colspan="2">Click to copy!</td></tr>
            </table>
        `;
    }
});

function studentToHtml(start, limit) {
    let allStudentTable = document.querySelector(".allStudentData table");
    for (let key = start; key <= limit; key++) {
        if (studentRollName[key] == undefined && key < 227) {
            let tableRow = document.createElement("tr");
            tableRow.innerHTML = `<td>${key}</td><td><span class="warning">THERE ARE NO STUDENT BY THIS ROLL</span></td>`;

            allStudentTable.appendChild(tableRow);
        } else if (studentRollName[key] == undefined && key >= 227) {
            let tableRow = document.createElement("tr");
            tableRow.innerHTML = `<td>${key}</td><td><span class="warning2">Student Name Will Insert Soon. (Help me by giving student name)</span></td>`;

            allStudentTable.appendChild(tableRow);
        } else {
            let tableRow = document.createElement("tr");
            tableRow.innerHTML = `<td>${key}</td><td>${studentRollName[key]}</td>`;

            allStudentTable.appendChild(tableRow);
        }
    }
}
studentToHtml(1, 247);

output.addEventListener("click", (e) => {
    let tmpTxt = lastSearchedData.std_roll + "    " + lastSearchedData.std_name;
    navigator.clipboard.writeText(tmpTxt);

    copyTxtAnimation();
});

function copyTxtAnimation() {
    document.querySelector(".popup").classList.add("animationStart");

    setTimeout(() => {
        document.querySelector(".popup").classList.remove("animationStart");
    }, 2400);
}
